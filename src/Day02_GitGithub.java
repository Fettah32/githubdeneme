public class Day02_GitGithub {
    /*
    1-) git init --> Local repo olusturmak icin yani .git ile klasorumuzun
    icindeki dosyalari iliskilendirmek icin kullanilir

    2-) git add . --> Working space'den (yani yerel) dosyalarimi staging area'ya (yani commitlemek icin beklenen yer)
    gonderir

    3-) git status --> git status --> Working space'deki ve staging area'daki durumu gosterir

    4-) git commit -m "mesaj" --> Staging area'dan commit store dosyalarimi gondermek icin kullanilir
    (commit yani versiyon yani surum olusturmus olurum)

    5-) git push --> Uzak repo'ya (yani remote - Github) gondermek icin kullandigimiz kod
    Yalniz git push komutunu direk kullanmak istersek bir kez
                git remote add origin adres
                git push -u origin master
    NOT : Yukaridaki iki komutu tek seferde kullandiktan sonra ikinci commit'lerim icin
    sadece git push kullaniriz.
     */
}
